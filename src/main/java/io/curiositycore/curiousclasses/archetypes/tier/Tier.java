package io.curiositycore.curiousclasses.archetypes.tier;

/**
 * Enum that represents the various tiers of archetypes.
 */
public enum Tier {
    /**
     * The first tier. This is typically the tier that new players will start at when first picking an Archetype.
     */
    TIER_ONE,
    /**
     * The central tier of the archetype system. This tier is usually where most gameplay and progression will be made.
     */
    TIER_TWO,
    /**
     * The end-game tier for archetypes. This is the highest achievable tier by any entity.
     */
    TIER_THREE;
}
